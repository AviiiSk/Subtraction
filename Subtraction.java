public class Subtraction {
	
	
	@SuppressWarnings("unused")
	public static void subtract(int a, int b)
	{
		int c  = a-b;
		
		System.out.println("Subtraction of Two Numbers : " + c);
		
		
	}
	

	public static void main(String[] args) {
		
		subtract(30, 15);
		
		

	}

}
